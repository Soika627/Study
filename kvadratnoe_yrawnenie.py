print("Решение квадратного уравнения ax² + bx + c = 0")

try:
    # Ввод коэффициентов
    a = float(input("Введите коэффициент a: "))
    b = float(input("Введите коэффициент b: "))
    c = float(input("Введите коэффициент c: "))
    
    print(f"\nУравнение: {a}x² + {b}x + {c} = 0")
    
    # Случай 1: a = 0 (линейное уравнение)
    if a == 0:
        print("Это линейное уравнение (a = 0)")
        
        if b == 0:
            if c == 0:
                print("Уравнение имеет бесконечно много решений: x ∈ ℝ")
            else:
                print("Уравнение не имеет решений")
        else:
            x = -c / b
            print(f"Уравнение имеет один корень: x = {x:.2f}")
    
    # Случай 2: a ≠ 0 (квадратное уравнение)
    else:
        # Вычисляем дискриминант
        D = b**2 - 4*a*c
        print(f"Дискриминант D = {D:.2f}")
        
        if D > 0:
            # Два различных действительных корня
            x1 = (-b + D**0.5) / (2*a)
            x2 = (-b - D**0.5) / (2*a)
            print(f"Уравнение имеет два различных действительных корня:")
            print(f"x₁ = {x1:.2f}")
            print(f"x₂ = {x2:.2f}")
        
        elif D == 0:
            # Один действительный корень (кратности 2)
            x = -b / (2*a)
            print(f"Уравнение имеет один действительный корень (кратности 2):")
            print(f"x = {x:.2f}")
        
        else:
            # Два комплексных корня
            real_part = -b / (2*a)
            imag_part = (-D)**0.5 / (2*a)
            print(f"Уравнение имеет два комплексных корня:")
            print(f"x₁ = {real_part:.2f} + {imag_part:.2f}i")
            print(f"x₂ = {real_part:.2f} - {imag_part:.2f}i")

except ValueError:
    print("Ошибка: пожалуйста, вводите только числа!")
except ZeroDivisionError:
    print("Ошибка: деление на ноль!")
except Exception as e:
    print(f"Произошла непредвиденная ошибка: {e}")
